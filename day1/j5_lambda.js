// Annonymous function assigned to variable
let f = function(x,y) {
    console.log("x is", x)
    console.log("y is", y)
    console.log("x + y is", x + y)
    console.log("----------------")
}

f(10,20)

// Lambda function
let f2 = (x,y) => {
    console.log("x is", x)
    console.log("y is", y)
    console.log("x + y is", x + y)
    console.log("----------------")
}

f2(10,20)